package day07_İfStatements;

public class C02_BasitIfStatements {

    public static void main(String[] args) {
        int sayı=10;
        if (sayı>0){
            System.out.println("sayı pozitif");
        }
        if (sayı%2==0) {
            System.out.println("sayı çift");
        }
        if (sayı%5==0) {
            System.out.println("sayı 5 in katı");
        }
    }
}
